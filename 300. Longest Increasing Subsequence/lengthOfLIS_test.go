package _00__Longest_Increasing_Subsequence

import "testing"

func TestLengthOfLIS(t *testing.T) {
	tests := [][]int{
		[]int{10, 9, 2, 5, 3, 7, 101, 18},
		[]int{10, 1, 2, 4, 7, 2},
		[]int{4, 2, 2, 2, 4, 4, 2, 2},
		[]int{9, 2, 5, 3, 7, 101, 1810, 1, 2, 4, 7, 210, 1, 2, 4, 7, 210, 1, 2, 4, 7, 210, 1, 2,
			4, 7, 210, 1, 2, 4, 7, 210, 1, 2, 4, 7, 210, 1, 2, 4, 7, 2},
		[]int{10, 9, 2, 5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2,
			5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101,
			1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 9, 2,
			5, 3, 7, 101, 1810, 9, 2, 5, 3, 7, 101, 1810, 1, 2, 4, 7, 210, 1, 2, 4, 7, 210, 1, 2, 4, 7, 210, 1, 2, 4, 7, 210, 1,
			2, 4, 7, 210, 1, 2, 4, 7, 210, 1, 2, 4, 7, 2},
	}

	results := []int{
		4,
		4,
		2,
		5,
		7,
	}
	caseNum := len(tests)
	for i := 0; i < caseNum; i++ {
		if ret := lengthOfLIS(tests[i]); ret != results[i] {
			t.Fatalf("case %d fails: %v\n", i, ret)
		}
	}
}
